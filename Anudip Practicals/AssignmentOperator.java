public class AssignmentOperator
{
	public static void main(String args[])
	{
	  int num1=100;
	  int num2=20;
	  num2=num1;
	  System.out.println("Output: "+num2);
	  num2+=num1;
	  System.out.println("+=Output: "+num2);
	  num2-=num1;
	  System.out.println("-=Output: "+num2);
	  num2*=num1;
	  System.out.println("*=Output: "+num2);
	  num2/=num1;
 	  System.out.println("/=Output: "+num2);
	  num2%=num1;
	  System.out.println("%=Output: "+num2);
	}
}
